package quizzlr.backend;

/**
 * This class is used to represent the progress of a user towards a particular
 * achievement. This data is not explicitly represented in the database, and is
 * instead computed on the fly.
 * @author David Lo
 *
 */
public class AchievementProgress {
    public final User user = null;
    public final AchievementType achievementType = null;
    public final int progress = 0;
    public final int total = 0;
}
